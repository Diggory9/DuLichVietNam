function makeUrl(photoId) {
  return `https://images.unsplash.com/photo-${photoId}?w=800&h=600&fit=crop&auto=format`;
}

const dataPath = path.join(__dirname, '../src/data/destinations.json');
const destinations = JSON.parse(fs.readFileSync(dataPath, 'utf-8'));

// Track usage per category to distribute evenly
const counters = {};
Object.keys(photoIds).forEach(k => { counters[k] = 0; });

destinations.forEach(dest => {
  const cat = dest.category;
  const pool = photoIds[cat] || photoIds['thien-nhien'];
  const poolLen = pool.length;

  // Get 2 different images for each destination
  const idx1 = counters[cat] % poolLen;
  const idx2 = (counters[cat] + 1) % poolLen;
  counters[cat] += 2;

  dest.images = [
    {
      src: makeUrl(pool[idx1]),
      alt: dest.name,
      caption: `${dest.name} – cảnh đẹp`,
    },
    {
      src: makeUrl(pool[idx2]),
      alt: `${dest.name} view`,
      caption: `Khung cảnh tại ${dest.name}`,
    },
  ];
});

fs.writeFileSync(dataPath, JSON.stringify(destinations, null, 2), 'utf-8');
console.log(`Updated images for ${destinations.length} destinations`);

// Show stats
Object.entries(counters).forEach(([cat, count]) => {
  const poolLen = (photoIds[cat] || []).length;
  console.log(`  ${cat}: ${count} images used, pool size: ${poolLen}`);
});
